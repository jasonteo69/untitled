public class Clothes {
    private String color;
    private String size;
    private String brand;

    public Clothes(String c, String s, String b) {
        color = c;
        size = s;
        brand = b;
    }

    public String getColor() {
        return color;
    }

    public String getSize() {
        return size;
    }

    public String getBrand() {
        return brand;
    }
}
